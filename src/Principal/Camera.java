package Principal;

public class Camera {
    
    private static int x = 0;
    private static int y = 0;

    public static void setX(int xx) {
        x = xx;
    }

    public static void setY(int yy) {
        y = yy;
    }

    public static int getX() {
        return x;
    }

    public static int getY() {
        return y;
    }
}
